// Helper function to get role-specific sidebar data
export const getSidebarDataByRole = (role?: string) => {
  if (role === "super_admin") {
    return SuperAdminSidebarData;
  } else if (role === "hospital_admin") {
    return HospitalAdminSidebarData;
  } else if (role === "doctor") {
    return DoctorSidebarData;
  }
  // Default/Admin sidebar
  return AdminSidebarData;
};

